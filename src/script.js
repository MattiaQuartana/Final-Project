document.addEventListener('DOMContentLoaded', function () {
    // Funzione per gestire il click su #1980
    function handleClick() {
        // Ottenere l'elemento #picturestime1980
        var picturestime1980 = document.getElementsByClassName('picturestime1980');
        
        // Verificare se le immagini di picturestime1980 sono visibili
        var isVisible = picturestime1980[0].style.display !== 'none';
        
        // Invertire la visibilità delle immagini di picturestime1980
        for (var i = 0; i < picturestime1980.length; i++) {
            picturestime1980[i].style.display = isVisible ? 'none' : 'block';
        }
       
        
        // Cambiare il colore del font di #1980 !!!
        this.style.color = isVisible ? 'white' : '#D83431';
    }

    // Nascondere le immagini di picturestime1980 all'avvio
    var picturestime1980 = document.getElementsByClassName('picturestime1980');
    for (var i = 0; i < picturestime1980.length; i++) {
        picturestime1980[i].style.display = 'none';
    }
    
    
    // Ottenere l'elemento #1980
    var element1980 = document.getElementById('year-1980');
    
    
    // Aggiungi un gestore di eventi al click su #1980
    element1980.addEventListener('click', handleClick);
    
});

